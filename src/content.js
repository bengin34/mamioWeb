export const localeKeys = Object.keys(locales);
export const seoPageIds = Object.keys(seoFeaturePages);

export function getLocaleFromPath(pathname = '/') {
  const firstSegment = pathname.split('/').filter(Boolean)[0];
  return localeKeys.includes(firstSegment) ? firstSegment : 'en';
}

export function getCanonical(lang, isRoot = false) {
  return isRoot ? `${DOMAIN}/` : `${DOMAIN}${locales[lang].path}`;
}

export function getSeoPagePath(pageId, lang) {
  const slug = seoFeaturePages[pageId]?.slugs?.[lang];
  return slug ? `${locales[lang].path}${slug}/` : null;
}

export function getSeoPageFromPath(pathname = '/') {
  const [lang, slug] = pathname.split('/').filter(Boolean);
  if (!seoPageLangs.includes(lang) || !slug) return null;
  const pageId = seoPageIds.find((id) => seoFeaturePages[id].slugs[lang] === slug);
  return pageId ? { kind: 'seoPage', lang, pageId } : null;
}

export function getRouteFromPath(pathname = '/') {
  const seoPage = getSeoPageFromPath(pathname);
  if (seoPage) return seoPage;
  return {
    kind: 'home',
    lang: getLocaleFromPath(pathname),
    isRoot: pathname === '/',
  };
}

export function getRouteCanonical(route) {
  if (route.kind === 'seoPage') {
    return `${DOMAIN}${getSeoPagePath(route.pageId, route.lang)}`;
  }
  return getCanonical(route.lang, route.isRoot);
}

export function getRouteAlternates(route) {
  if (route.kind === 'seoPage') {
    return [
      ...seoPageLangs.map((lang) => ({
        lang,
        href: `${DOMAIN}${getSeoPagePath(route.pageId, lang)}`,
      })),
      {
        lang: 'x-default',
        href: `${DOMAIN}${getSeoPagePath(route.pageId, 'en')}`,
      },
    ];
  }

  return [
    ...localeKeys.map((lang) => ({
      lang,
      href: `${DOMAIN}${locales[lang].path}`,
    })),
    {
      lang: 'x-default',
      href: `${DOMAIN}/`,
    },
  ];
}
